/*==========================================================
                PROFITABILITY ANALYSIS
==========================================================*/


/*----------------------------------------------------------
Business Question:
Which products are generating losses?

Business Value:
Helps management identify products requiring pricing,
discount, or inventory review.
-----------------------------------------------------------*/

SELECT TOP 20

P.Product_Name,

SUM(F.Profit) AS TotalLoss

FROM FactSales F

JOIN DimProduct P
ON F.ProductKey = P.ProductKey

GROUP BY P.Product_Name

HAVING SUM(F.Profit) < 0

ORDER BY TotalLoss;



/*----------------------------------------------------------
Business Question:
How does discount affect profit?

Business Value:
Evaluates whether high discounts reduce profitability.
-----------------------------------------------------------*/

SELECT

Discount,

SUM(Sales) AS Revenue,

SUM(Profit) AS Profit

FROM FactSales

GROUP BY Discount

ORDER BY Discount;



/*----------------------------------------------------------
Business Question:
Which categories are most profitable?

Business Value:
Supports strategic planning and resource allocation.
-----------------------------------------------------------*/

SELECT

P.Category,

ROUND(
SUM(F.Profit)*100.0/SUM(F.Sales),2
) AS ProfitMargin

FROM FactSales F

JOIN DimProduct P
ON F.ProductKey = P.ProductKey

GROUP BY P.Category

ORDER BY ProfitMargin DESC;

/*==========================================================
                SHIPPING ANALYSIS
==========================================================*/


/*----------------------------------------------------------
Business Question:
Which shipping mode generates the highest revenue?

Business Value:
Evaluates customer shipping preferences and operational
performance.
-----------------------------------------------------------*/

SELECT

ShipMode,

SUM(Sales) AS Revenue,

SUM(Profit) AS Profit,

COUNT(OrderID) AS Orders

FROM FactSales

GROUP BY ShipMode

ORDER BY Revenue DESC;